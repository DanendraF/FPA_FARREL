public class ForTraversal {
    public static void main(String[] args) throws Exception {
        
        //naik
        System.out.println("Naik");
        for (int i=1; i<=5; i++) {
            System.out.println(i);
        }
        System.out.println();
        //turun
        System.out.println("Turun");
        for(int i=5; i>=1; i--){
            System.out.println(i);
        }
          
    }
}
